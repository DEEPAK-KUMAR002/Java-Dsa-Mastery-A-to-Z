package ch03_Operators;

public class tut03_Relational {
    public static void main(String[] args) {
        int a=10;
        int b=10;
        System.out.println("(A==B): "+(a==b));
        System.out.println("(A!=B): "+(a!=b));
        System.out.println("(A>B): "+(a>b));
        System.out.println("(A>=B): "+(a>=b));
        System.out.println("(A<B): "+(a<b));
        System.out.println("(A<=B): "+(a<=b));

    }
}
